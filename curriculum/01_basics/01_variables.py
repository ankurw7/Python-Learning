"""
Topic: Variables and Data Types
"""

def run():
    # Integer
    count = 10
    print(f"Count is an integer: {count}")

    # Float
    pi = 3.14159
    print(f"Pi is a float: {pi}")

    # String
    greeting = "Hello, AI!"
    print(f"Greeting is a string: {greeting}")

    # Boolean
    is_active = True
    print(f"Is Active is a boolean: {is_active}")

if __name__ == "__main__":
    run()
